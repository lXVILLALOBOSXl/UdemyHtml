// Orden de las operaciones
let resultado = 10;
let resultado2 = 20;

console.log(resultado);
console.log(resultado2);

resultado = 20 + 30 * 2;
console.log(resultado);
resultado = (20+30) * 2;
console.log(resultado);
resultado = 10;

//incrementos
console.log(resultado++);
console.log(resultado);
console.log(++resultado2);
console.log(resultado2);
console.log(resultado--);
console.log(resultado);
console.log(--resultado2);
console.log(resultado2);
resultado += 10;
console.log(resultado);
